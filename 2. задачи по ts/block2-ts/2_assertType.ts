// Проверка типов на лету

function assertType<T>(value: unknown): value is T {
    return true; // Проверка типов выполняется TypeScript во время компиляции
  }
  
  console.log(assertType<number>(42)); // true
  console.log(assertType<string>(42)); // false
  
  /*
  Пояснение:

assertType — это "type guard", который позволяет TypeScript понимать, что значение соответствует указанному типу.
Встроенные проверки типов выполняются статически при компиляции. Если значение не соответствует указанному типу, TypeScript выдаёт ошибку во время разработки.
Эта функция может быть расширена для более сложных проверок, например, проверки структур объектов.
*/