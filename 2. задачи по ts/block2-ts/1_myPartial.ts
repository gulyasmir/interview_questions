// Собственная реализация Partial

type MyPartial<T> = {
    [P in keyof T]?: T[P];
  };
  
  type Example = { a: number; b: string };
  const partial: MyPartial<Example> = { a: 42 };
  
  /*
  Пояснение:

MyPartial<T> делает все свойства типа необязательными с помощью синтаксиса mapped types ([P in keyof T]).
Каждый ключ объекта T становится необязательным (?), сохраняя свои исходные типы значений.
Это полезно для частичного изменения объектов, например, при обновлении данных.
*/