function linearSearch(arr, target) {
    for (let i = 0; i < arr.length; i++) {
      if (arr[i] === target) {
        return i; // –Ω–∞—à–ª–∏ —ç–ª–µ–º–µ–Ω—Ç
      }
    }
    return -1; // –Ω–µ –Ω–∞—à–ª–∏ —ç–ª–µ–º–µ–Ω—Ç
  }
  
